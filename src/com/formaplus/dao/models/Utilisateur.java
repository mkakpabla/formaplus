package com.formaplus.dao.models;



public class Utilisateur {
	
	private int idUtr;
	
	private String nomCompUtr;
	private String loginUtr;
	private String mdpUtr;
	
	
	
	public int getIdUtr() {
		return idUtr;
	}
	public void setIdUtr(int idUtr) {
		this.idUtr = idUtr;
	}
	public String getNomCompUtr() {
		return nomCompUtr;
	}
	public void setNomCompUtr(String nomCompUtr) {
		this.nomCompUtr = nomCompUtr;
	}
	public String getLoginUtr() {
		return loginUtr;
	}
	public void setLoginUtr(String loginUtr) {
		this.loginUtr = loginUtr;
	}
	public String getMdpUtr() {
		return mdpUtr;
	}
	public void setMdpUtr(String mdpUtr) {
		this.mdpUtr = mdpUtr;
	}
	
	
	

}
